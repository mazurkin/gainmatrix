package com.gainmatrix.lib.task;

public class ServiceTaskInvokerException extends Exception {

    public ServiceTaskInvokerException() {
    }

    public ServiceTaskInvokerException(String message) {
        super(message);
    }

    public ServiceTaskInvokerException(String message, Throwable cause) {
        super(message, cause);
    }

    public ServiceTaskInvokerException(Throwable cause) {
        super(cause);
    }

}
